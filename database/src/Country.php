<?php
class Country
{
    private $id;
    private $name;
    private $phone_code;
    private $code;


    public function getId ()
    {
        return $this->id;
    }


    public function getName ()
    {
        return $this->name;
    }


    public function getPhoneCode ()
    {
        return $this->phone_code;
    }


    public function getCode ()
    {
        return $this->code;
    }




}